void LCD_CD_DATA(unsigned int val)
{
      RS = 1;			  
      RDB = 1;			 
      WRB = 1;			
      TFT_CS = 0;	
      DataH = val>>8;

      DataL = val;
      WRB = 0;				
      WRB = 1;			  
      TFT_CS = 1;
}

void LCD_CD_REG(unsigned int reg)
{
      RS = 0;			
      RDB = 1;			
      WRB = 1;				
      TFT_CS = 0;			 
      DataH = reg>>8;

      DataL = reg;
      WRB = 0;			   
      WRB = 1;			  
      TFT_CS = 1;
}

